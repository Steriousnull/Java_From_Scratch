package Learning;

public class Loops {
	public static void main(String[] args) {
		int g=1;
		for (int i = 1;i<=5;i++) {
			System.out.println("%");
		}
	
	
		while(g<=10) {
			System.out.println("CSP");
			System.out.println(g+",");
			g++;
		}
	}
}
		
